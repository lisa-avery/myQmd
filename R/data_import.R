#' Inspect variables in a data frame
#'
#' @description
#' Returns a tibble of per-variable metadata: position, name, label, class,
#' missingness, and unique-value count. Useful as a quick overview of an
#' imported data frame, and as the basis for \code{create_dictionary()}.
#'
#' @param data A data frame.
#'
#' @return A tibble with one row per column and the following columns:
#'   \describe{
#'     \item{column_number}{Position of the column in \code{data}.}
#'     \item{variable}{Column name.}
#'     \item{label}{The column's \code{label} attribute, or \code{NA}.}
#'     \item{type}{One of \code{"Date"}, \code{"Numeric"}, \code{"Logical"},
#'       \code{"Categorical"} (for factors), or \code{"Character"}.}
#'     \item{n_missing}{Number of \code{NA} values.}
#'     \item{n_unique}{Number of distinct non-\code{NA} values.}
#'     \item{all_missing}{\code{TRUE} if every value is \code{NA}.}
#'   }
#'
#' @examples
#' \dontrun{
#' df <- read_excel_no_dictionary("data.xlsx", "Sheet1")
#' get_var_info(df)
#' }
#'
#' @export
get_var_info <- function(data) {
  if (!is.data.frame(data)) stop("`data` must be a data frame.")

  classify <- function(col) {
    if (inherits(col, "Date") || inherits(col, "POSIXt")) "Date"
    else if (is.factor(col)) "Categorical"
    else if (is.logical(col)) "Logical"
    else if (is.numeric(col)) "Numeric"
    else "Character"
  }

  tibble::tibble(
    column_number = seq_along(data),
    variable      = names(data),
    label         = vapply(data, function(col) {
      l <- attr(col, "label")
      if (is.null(l)) NA_character_ else as.character(l)[1]
    }, character(1)),
    type          = vapply(data, classify, character(1)),
    n_missing     = vapply(data, function(col) sum(is.na(col)), integer(1)),
    n_unique      = vapply(data, function(col) length(unique(col[!is.na(col)])), integer(1)),
    all_missing   = vapply(data, function(col) all(is.na(col)), logical(1))
  )
}


#' Drop uninformative columns from a data frame
#'
#' @description
#' Removes columns that carry no analytic value: columns where every value
#' is \code{NA}, and high-cardinality character/factor columns (free text)
#' where the number of distinct values exceeds \code{factor_limit}. Intended
#' as a preprocessing step before \code{create_dictionary()}.
#'
#' @param data A data frame.
#' @param factor_limit Integer. Character or factor columns with more than
#'   this many unique values are treated as free text and dropped. Default
#'   \code{10}.
#' @param verbose Logical. If \code{TRUE} (default), report what was
#'   dropped.
#'
#' @return The input data frame with uninformative columns removed.
#'
#' @examples
#' \dontrun{
#' df <- read_excel_no_dictionary("data.xlsx", "Sheet1")
#' df <- clean_data(df)
#' create_dictionary(df, "scaffolded.xlsx")
#' }
#'
#' @export
clean_data <- function(data, factor_limit = 10, verbose = TRUE) {
  if (!is.data.frame(data)) stop("`data` must be a data frame.")
  info <- get_var_info(data)

  to_drop <- info$variable[info$all_missing]
  is_text_heavy <- info$type %in% c("Character", "Categorical") &
                   info$n_unique > factor_limit
  to_drop <- unique(c(to_drop, info$variable[is_text_heavy]))

  if (length(to_drop) > 0) {
    msg_if(verbose, sprintf("Dropping %d uninformative column(s): %s",
                            length(to_drop),
                            paste(to_drop, collapse = ", ")))
    data <- data[, setdiff(names(data), to_drop), drop = FALSE]
  } else {
    msg_if(verbose, "No uninformative columns to drop.")
  }
  data
}


#' Scaffold a data dictionary from a data frame
#'
#' @description
#' Writes a data dictionary in the schema expected by
#' \code{read_excel_with_dictionary()}. The dictionary captures each
#' column's original name, a tidy suggested name, a label, an inferred
#' type, and (for categorical columns) the observed levels. The user can
#' then edit the resulting \code{.xlsx} in Excel - typically to refine
#' labels, populate \code{Value_Label}, set \code{Import = FALSE} on
#' columns to skip - and re-import via \code{read_excel_with_dictionary()}.
#'
#' @details
#' Type is inferred from each column's class:
#' \itemize{
#'   \item Date or POSIXt columns -> \code{Date}
#'   \item Numeric columns -> \code{Numeric}
#'   \item Logical, factor, or character columns with at most
#'     \code{factor_limit} unique values -> \code{Categorical} (with one
#'     row per observed level)
#'   \item Character columns with more than \code{factor_limit} unique
#'     values -> \code{Type} left blank (imported as character)
#' }
#'
#' If \code{data_sheet} is non-\code{NULL}, the data itself is written to
#' that sheet of the same workbook, so the resulting file is a complete
#' self-contained input for \code{read_excel_with_dictionary()}.
#'
#' \code{Label_For_Report} defaults to each column's \code{label} attribute
#' if one is present (as set by \code{set_names_as_labels()} or
#' \code{read_excel_no_dictionary()}), otherwise to the column name.
#' \code{Suggested_Name} defaults to a tidy version of the column name via
#' \code{janitor::make_clean_names()}.
#'
#' @param data A data frame to describe.
#' @param file Path for the output \code{.xlsx} file.
#' @param dictionary_sheet Character. Name of the dictionary sheet.
#'   Default \code{"Dictionary"}.
#' @param data_sheet Character or \code{NULL}. If non-\code{NULL}, the data
#'   is also written to a sheet of this name in the same workbook. Default
#'   \code{"Data"}.
#' @param factor_limit Integer. Columns with more than this many unique
#'   values are not enumerated as categorical. Default \code{10}.
#' @param verbose Logical. If \code{TRUE} (default), emit a summary message.
#'
#' @return Invisibly, the dictionary tibble that was written. Called for
#'   its side effect of writing \code{file}.
#'
#' @examples
#' \dontrun{
#' df <- read_excel_no_dictionary("raw.xlsx", "Sheet1")
#' df <- clean_data(df)
#' create_dictionary(df, "scaffolded.xlsx")
#'
#' # User edits scaffolded.xlsx in Excel to refine labels and value labels
#'
#' coded <- read_excel_with_dictionary(
#'   "scaffolded.xlsx",
#'   data_sheet = "Data",
#'   dictionary_sheet = "Dictionary"
#' )
#' }
#'
#' @importFrom writexl write_xlsx
#' @importFrom janitor make_clean_names
#' @export
create_dictionary <- function(data,
                              file,
                              dictionary_sheet = "Dictionary",
                              data_sheet = "Data",
                              factor_limit = 10,
                              verbose = TRUE) {
  if (!is.data.frame(data)) stop("`data` must be a data frame.")
  if (missing(file) || !nzchar(file)) {
    stop("`file` must be a valid output path.")
  }

  info <- get_var_info(data)

  # Build the dictionary one variable at a time ----
  rows <- vector("list", nrow(info))
  for (i in seq_len(nrow(info))) {
    v        <- info$variable[i]
    col_data <- data[[v]]
    cls      <- info$type[i]
    n_uniq   <- info$n_unique[i]
    label    <- if (is.na(info$label[i])) v else info$label[i]
    suggested <- janitor::make_clean_names(v)

    # Decide the schema Type from the inferred class ----
    if (cls == "Date") {
      schema_type <- "Date"
    } else if (cls == "Numeric") {
      schema_type <- "Numeric"
    } else if (cls == "Logical") {
      schema_type <- "Categorical"
    } else if (cls %in% c("Character", "Categorical") && n_uniq <= factor_limit) {
      schema_type <- "Categorical"
    } else {
      # Free text - leave Type blank so the importer treats it as character
      schema_type <- NA_character_
    }

    # Header row for this variable ----
    header <- tibble::tibble(
      Current_Variable_Name = v,
      Suggested_Name        = suggested,
      Label_For_Report      = label,
      Type                  = schema_type,
      Column_Number         = info$column_number[i],
      Import                = TRUE,
      Value                 = NA,
      Value_Label           = NA_character_
    )

    # For Categorical types: enumerate observed levels, one extra row each ----
    if (identical(schema_type, "Categorical")) {
      lvls <- sort(unique(col_data[!is.na(col_data)]))
      if (length(lvls) > 0) {
        # First level goes on the header row; remaining levels get continuation rows
        header$Value <- lvls[1]
        header$Value_Label <- NA_character_
        if (length(lvls) > 1) {
          cont <- tibble::tibble(
            Current_Variable_Name = NA_character_,
            Suggested_Name        = NA_character_,
            Label_For_Report      = NA_character_,
            Type                  = NA_character_,
            Column_Number         = NA_integer_,
            Import                = NA,
            Value                 = lvls[-1],
            Value_Label           = NA_character_
          )
          rows[[i]] <- dplyr::bind_rows(header, cont)
        } else {
          rows[[i]] <- header
        }
      } else {
        rows[[i]] <- header
      }
    } else {
      rows[[i]] <- header
    }
  }

  dictionary <- dplyr::bind_rows(rows)

  # Coerce Value to character for consistent Excel writing ----
  # (mixed numeric/character levels across variables would otherwise clash)
  dictionary$Value <- as.character(dictionary$Value)

  # Write the workbook ----
  sheets <- list()
  sheets[[dictionary_sheet]] <- dictionary
  if (!is.null(data_sheet)) {
    # writexl can't write Date-class with attributes cleanly across all types;
    # tibble handles the conversion safely.
    sheets[[data_sheet]] <- tibble::as_tibble(data)
  }
  writexl::write_xlsx(sheets, path = file)

  msg_if(verbose, sprintf(
    "Wrote dictionary for %d column(s) to '%s'%s. Edit and re-import with read_excel_with_dictionary().",
    nrow(info), file,
    if (!is.null(data_sheet)) sprintf(" (data on sheet '%s', dictionary on sheet '%s')",
                                       data_sheet, dictionary_sheet) else ""
  ))

  invisible(dictionary)
}
