}


#' Per-variable summary of an import
#'
#' @description
#' Builds a tidy tibble summarising what happened to each variable during
#' import. Designed to be embedded directly in a report via
#' \code{knitr::kable()} or \code{gt::gt()}.
#'
#' @details
#' The summary combines information from the dictionary (when present),
#' the data classes after import, and the numeric/date conversion
#' diagnostics. Columns:
#' \describe{
#'   \item{variable}{Column name.}
#'   \item{label}{Column label, if set.}
#'   \item{type}{Resolved variable type: from the dictionary's \code{Type}
#'     field when available, otherwise inferred from the column class.}
#'   \item{action}{Short description of what the import did
#'     (e.g. "2 non-numeric values removed", "converted to Date",
#'     "factor with 3 levels", or "-" when no notable action was taken).}
#'   \item{n_issues}{Count of issues raised for the column (non-numeric
#'     coercions, date errors, future dates, etc.).}
#' }
#'
#' @param x A data frame returned by one of the importExcel reader
#'   functions.
#'
#' @return A tibble with one row per variable.
#'
#' @examples
#' \dontrun{
#' df <- read_excel_with_dictionary("data.xlsx", "Data", "Dict")
#' knitr::kable(import_summary(df))
#' }
#'
#' @export
import_summary <- function(x) {
  if (!is.data.frame(x)) stop("`x` must be a data frame.")

  # Base table from the data frame itself ----
  out <- tibble::tibble(
    variable = names(x),
    label    = vapply(x, function(col) {
      l <- attr(col, "label")
      if (is.null(l)) NA_character_ else as.character(l)[1]
    }, character(1)),
    type     = vapply(x, function(col) {
      if (inherits(col, "Date") || inherits(col, "POSIXt")) "Date"
      else if (is.factor(col)) "Categorical"
      else if (is.numeric(col)) "Numeric"
      else if (is.logical(col)) "Logical"
      else "Character"
    }, character(1)),
    action   = "-",
    n_issues = 0L
  )

  # Override type from the dictionary if available ----
  dict <- import_dictionary(x)
  if (!is.null(dict) && "Suggested_Name" %in% names(dict) && "Type" %in% names(dict)) {
    dict_lookup <- dict |>
      dplyr::filter(!is.na(Suggested_Name)) |>
      dplyr::select(Suggested_Name, Type) |>
      dplyr::distinct()
    out <- out |>
      dplyr::left_join(dict_lookup, by = c("variable" = "Suggested_Name")) |>
      dplyr::mutate(type = dplyr::coalesce(Type, type)) |>
      dplyr::select(-Type)
  }

  # Fill in factor level counts for categorical/coded columns ----
  for (i in seq_along(x)) {
    if (is.factor(x[[i]]) && out$action[i] == "-") {
      out$action[i] <- sprintf("factor with %d levels", length(levels(x[[i]])))
    }
    if (inherits(x[[i]], "Date") && out$action[i] == "-") {
      out$action[i] <- "converted to Date"
    }
  }

  # Layer in numeric conversion diagnostics ----
  nc <- numeric_conversions(x)
  if (!is.null(nc) && nrow(nc) > 0) {
    for (j in seq_len(nrow(nc))) {
      i <- which(out$variable == nc$variable[j])
      if (length(i) == 1) {
        out$action[i]   <- sprintf("%d non-numeric value(s) removed", nc$n[j])
        out$n_issues[i] <- nc$n[j]
      }
    }
  }

  # Layer in date warning diagnostics ----
  dw <- date_warnings(x)
  if (!is.null(dw) && nrow(dw) > 0) {
    for (j in seq_len(nrow(dw))) {
      i <- which(out$variable == dw$variable[j])
      if (length(i) == 1) {
        parts <- character(0)
        if (dw$n_errors[j] > 0)
          parts <- c(parts, sprintf("%d unparseable", dw$n_errors[j]))
        if (isTRUE(dw$multiple_formats[j]))
          parts <- c(parts, "mixed formats")
        if (dw$n_future_dates[j] > 0)
          parts <- c(parts, sprintf("%d future date(s)", dw$n_future_dates[j]))
        if (length(parts) > 0) {
          out$action[i]   <- paste("Date;", paste(parts, collapse = "; "))
          out$n_issues[i] <- dw$n_errors[j] + dw$n_future_dates[j]
        }
      }
    }
  }

  out
}


